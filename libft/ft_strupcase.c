#include "./includes/libft.h"

char	*ft_strupcase(char *str)
{
	int	j;

	j = 0;
	while (str[j] != '\0')
	{
		if (str[j] >= 'a' && str[j] <= 'z')
			str[j] = str[j] - 32;
		j++;
	}
	return (str);
}
