#include "./includes/libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t nbr)
{
	const unsigned char *str1;
	const unsigned char *str2;

	str1 = (const unsigned char *)s1;
	str2 = (const unsigned char *)s2;
	if (str1 == str2 || nbr == 0)
		return (0);
	while (nbr--)
	{
		if (*str1 != *str2)
			return (*str1 - *str2);
		else
		{
			str1++;
			str2++;
		}
	}
	return (0);
}
